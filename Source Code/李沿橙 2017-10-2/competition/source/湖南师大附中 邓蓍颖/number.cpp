#include<bits/stdc++.h>
using namespace std;
int n;long long ans;
int main()
{
	freopen("number.in","r",stdin);
	freopen("number.out","w",stdout);
	int i;
	scanf("%d",&n);
	ans+=(n/10-1)*(n/10-2)/2;//12,21
	if(n/10-1>0)ans+=min(n%10,n/10-1);//43,34上一种的边界情况，一百+可以视作1 43,1 34；
	if(n>=(n/100)*100+(n/100)*10+n/100)ans+=n/100;
	if(n>=(n/1000)*1000+(n/1000)*100+(n/1000)*10+n/1000)ans+=n/1000;
	if(n>=(n/10000)*10000+(n/10000)*1000+(n/10000)*100+(n/10000)*10+n/10000)ans+=n/10000;
	if(n>=(n/100000)*100000+(n/100000)*1000+(n/100000)*100+(n/100000)*10+(n/100000))ans+=n/100000;
	if(n>=(n/1000000)*1000000+(n/1000000)*100000+(n/1000000)*10000+(n/1000000)*1000+(n/1000000)*100+(n/1000000)*10+n/1000000)ans+=n/1000000;
	if(n>=(n/10000000)*10000000+(n/10000000)*1000000+(n/10000000)*100000+(n/10000000)*10000+(n/10000000)*1000+(n/10000000)*100+(n/10000000)*10+n/10000000)ans+=n/10000000;
	i=10;
	while(i<=10000000)
	{
		if(i>n)break;
		ans+=(n/i-1); 
		if(n>=(n/i)*i+n/i)ans++;
		i*=10;	
	} 
	printf("%lld",ans);
	fclose(stdin);
	fclose(stdout);
	return 0;
}
